public class Mathh
{
    public float div(int a,int b)
    {
        return a/b;
    }

    public double circum(int a)
    {
        return 2*3.14*a;
    }

    public char letter(String word)
    {
        return word.charAt(0);
    }

    public String statement(String word)
    {
        return "There are "+word;
    }

    public boolean even(int b)
    {
        return b%2==0;
    }



}